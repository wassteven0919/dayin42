char	*ft_strdup(char *src)
{
	int i;
	char *asd;

	i = 0;
	while (src[i] != '\0')
		i++;
	asd = malloc(i);
	i = 0;
	while (src[i] != '\0')
	{
		asd[i] = src[i];
		i++;
	}
	asd[i] = '\0';
	return (asd);
}
