char	*ft_strncat(char *dest, char *src, int nb)
{
	int a;
	int f;

	a = 0;
	f = 0;
	while (dest[a] != '\0')
		a++;
	while (src[b] != '\0' && b < n)
	{
		dest[a] = src[b];
		a++;
		b++;
	}
	dest[a] = '\0';
	return (dest);
}
