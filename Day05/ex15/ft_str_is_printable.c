int	ft_str_is_printable(char *str)
{
	int a;
	int f;

	a = 0;
	f = 0;
	while (str[a] != '\0')
	{
		if (str[a] >= 32 || str[a] <= 126)
			f = f;
		else
			f++;
		a++;
	}
	if (f)
		return (0);
	else
		return (1);
}
