int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int cnt;

	cnt = 0;
	if (*s1 == '\0' && *s2 != 0)
		return (*s1 - *s2);
	if (*s2 == '\0' && *s1 != 0)
		return (*s1 - *s2);
	while (*s1 != '\0' && *s2 != '\0' && cnt < n)
	{
		if ((*s1 - *s2) > 0)
			return (*s1 - *s2);
		else if ((*s1 - *s2) < 0)
			return (*s1 - *s2);
		s1++;
		s2++;
		cnt++;
	}
	return (0);
}
