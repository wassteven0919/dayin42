int	ft_str_is_alpha(char *str)
{
	int a;
	int d;

	a = 0;
	d = 0;
	if (str[a] == '\0')
	{
		return (1);
	}
	while (str[a] != '\0')
	{
		if (str[a] < 65 || (str[a] > 90 && str[a] < 97) || str[a] > 122)
		{
			d++;
		}
		a++;
	}
	if (d)
		return (1);
	else
		return (0);
}
